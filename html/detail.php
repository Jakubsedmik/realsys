<p>Nabízím k prodeji bytovou jednotku v osobním vlastnictví 2+kk v bytovém domě MUSILKA (kolaudace 07/2014) ve vyhledávané brněnské lokalitě – městské části Brno-město Husovice.</p>

<p>Jedná se o prodej energeticky úsporné novostavby bytu 2+kk s lodžií v bytovém projektu MUSILKA, který je orientovaný na jihozápadní stranu do vnitrobloku se zelení. Bytový dům MUSILKA je moderní pasivní dům s minimálními provozními náklady. Byt se nachází ve 2.NP s výtahem (uprostřed - sousedé z každé strany) 5ti poschoďového bytového domu. Celková plocha bytu včetně příslušenství je 49,84 m2 a zahrnuje: předsíň s úložným koutem (7,34 m2), obývací místnost s kuchyní (22,03 m2), ložnici (11,14 m2) koupelnu s WC (3,00 m2), úložný podkrovní box (2,37 m2), lodžii (3,96 m2), podíl na společné zahradě. V bytě jsou použity výhradně kvalitní materiály a je částečně zařízený: obývací místnost s kuchyňským koutem je vybavena novou kuchyňskou linkou (sklokeramická varná deska, horkovzdušná trouba, kombinovaná chladnička, odsavač par).</p>

